from lib_calc import operacoes

def add(a: float, b: float) -> float:
    return operacoes.add(a, b)

def subtract(a: float, b: float) -> float:
    return operacoes.subtract(a, b)

def multiply(a: float, b: float) -> float:
    return operacoes.multiply(a, b)

def divide(a: float, b: float) -> float:
    return operacoes.divide(a, b)

def exponent(a: float, b: float) -> float:
    return operacoes.exponent(a, b)

def sqrt(a: float) -> float:
    return operacoes.sqrt(a)

def factorial(a: float) -> int:
    return operacoes.factorial(a)